int lcm(int a, int b)
{
	int x = a;
	int y = b;
	int r;

	if (a <= 0 || b <= 0)
		return 0;

	while (y != 0)
	{
		r = x % y;
		x = y;
		y = r;
	}
	return ((a * b) / x);
}
