#include "unistd.h"

char	*ft_strcpy(char *s1, char *s2)
{
	int	cnt;

	cnt = 0;
	while (s1[cnt] != '\0')
	{
		s2[cnt] = s1[cnt];
		cnt++;
	}
	s2[cnt] = '\0';
	return (s2);
}
